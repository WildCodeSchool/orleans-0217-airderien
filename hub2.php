        <h3 class="h1-like txtcenter">Un peu d&rsquo;histoire</h3>
        <div class="wp-content">
            <p>La Compagnie du Petit Matin, c’est une compagnie professionnelle de théâtre née en Janvier 2003 à Toulouse.</p>
            <p>Depuis le début, nous travaillons autour de deux axes : la création et la formation. Nous avons confié ces premières années à Bruno Abadie la responsabilités des mises en scène des spectacles professionnels ainsi que celle du Pôle Formation. La Compagnie a posé ses valises en septembre 2003 au Centre d’Animation de Saint Simon, un quartier de Toulouse.</p>
            <p>Elle bénéficie d’un prêt de la salle sous forme de convention avec La Mairie de Toulouse, ce qui nous permet de pratiquer des tarifs attractifs et d’attirer des élèves issus de tous milieux socioculturels.</p>
            <p>Même s’il ne s’agit pas à proprement parler d’une École de Théâtre, la conception de nos pratiques permet de mettre en place une vraie fidélité. Certains élèves nous suivent ainsi depuis le début et grandissent avec nous… D’autres ont même franchi le cap en devenant professionnels à leur tour.</p>
            <p>Nous avons fêté le 14 septembre 2013 nos 10 ans par une longue soirée au Centre d&rsquo;animation de St Simon de Toulouse ou une douzaine d&rsquo;artistes professionnels ont présenté des textes et formes inédites.</p>
            <p>Par ailleurs, La Cie dirige des ateliers de pratique artistique sur des collèges ou écoles Primaires sur Toulouse. Depuis plusieurs années, elle est sélectionnée par La Mairie de Toulouse pour participer aux Passeports pour l&rsquo;art proposés aux écoles de la ville.</p>
